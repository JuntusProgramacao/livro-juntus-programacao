#22. Durante uma viagem a Inglaterra você foi obrigado a comprar um termômetro, 
# e verificou que este estava marcando a temperatura em Fahrenheit. Faça um algoritmo 
# para calcular a conversão de graus Celcius para Fahrenheit.

celsius = float(input("Digite a temperatura em graus Celsius: "))
fahrenheit = (celsius * 9/5) + 32
print("A temperatura em graus Fahrenheit é:", fahrenheit)


