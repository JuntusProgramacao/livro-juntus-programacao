public class Exercicio14 {
    public static void main(String[] args) {
        // Faça um algoritmo para imprimir os números pares entre 8 e 18

        System.out.println("Este programa vai imprimir os números pares entre 8 e 18");

        for (int i = 8; i <= 18; i++) {
            if (i % 2 == 0) {
                System.out.println(i);
            }
        }
    }
}