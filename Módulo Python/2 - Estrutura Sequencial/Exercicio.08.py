#8. Faça um programa para calcular o cubo de um número informado pelo usuário. 
# Em seguida,faça a diferença desse resultado com o número lido.

numero = int(input('Digite um número: '))
cubo = numero * numero * numero
print('Resultado do cubo do número:', cubo)
print('Resultado da diferença do cubo e do número:', cubo - numero)


